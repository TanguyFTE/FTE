                                            <p style="text-align: justify"><strong style="font-size: 1.5em;">-></strong> Le premier, relativement bien médiatisé, est l'émission de CO2 qui met en péril nos modes de vie. En effet, la plupart de nos sources d'énergie émettent des gaz à effet de serre qui, selon leur définition, réchauffent la basse atmosphère. Ce réchauffement a de nombreuses conséquences, telles que l'augmentation de la fréquence des sécheresses dans le bassin méditerranéen ou encore la banalisation des canicules. Ces effets menacent l'agriculture en France, les infrastructures, mais aussi la vie des plus fragiles. Dans des pays plus au sud, les températures peuvent dépasser les 36°C pendant plusieurs mois, rendant la survie humaine impossible. Nous parlons ici de conséquences qui affectent notre génération, et plus seulement les générations futures.</p>                                           
                                            <h6>Pour contourner ce premier facteur limitant, il y a la transition énergétique.</h6>
                                            <p style="text-align: justify"><strong style="font-size: 1.5em;">-></strong> Le second est très peu évoqué dans les médias, il s'agit de l'épuisement des ressources. Avec une consommation de pétrole de près de 100 000 000 de barils par jour, il paraît évident que nous n'en aurons pas 'ad vitam aeternam'. Nous allons avoir de moins en moins d'énergies fossiles disponibles au fil du temps. La transition énergétique devra donc avoir lieu, et ce même sans prendre en compte les considérations climatiques.</p>
