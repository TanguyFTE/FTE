                                                <h6>-> La branche sociale :</h6>
                                                <p style="text-align: justify;">Elle découle entièrement de la première branche. <br /> Depuis 150 ans, le pouvoir d'achat n'a fait qu'augmenter parallèlement à l'augmentation de l'utilisation d'énergie. Il est bien évident que la transition énergétique ne pourra garantir un tel niveau de consommation d'énergie, donc un tel pouvoir d'achat. Chez FTE, notre objectif est donc de limiter cette baisse au minimum. Ce serait prendre les Français pour des imbéciles en leur disant que le PIB, donc le pouvoir d'achat, peut croître indéfiniment. Chez FTE, nous ne souhaitons pas la décroissance, cependant, elle est inévitable. Nous pensons donc qu'elle sera plus supportable si elle est voulue plutôt que subie.</p>                                            
